def get_faq_text(key: str, locale: str = 'rus') -> str:
    """
    Получить текст FAQ по ключу и языку.

    Args:
        key: Ключ текста (например 'lvl6_1_2')
        locale: Язык ('rus' или 'kaz')

    Returns:
        Текст на нужном языке или русский если перевода нет
    """
    locale_norm = (locale or 'rus').lower().strip()
    # Часто в БД/сессии встречается сокращение "kz" вместо "kaz".
    if locale_norm == 'kz':
        locale_norm = 'kaz'
    if locale_norm in ('ru', 'russian'):
        locale_norm = 'rus'

    translations = FAQ_TRANSLATIONS.get(key, {})
    return translations.get(locale_norm, translations.get('rus', ''))
